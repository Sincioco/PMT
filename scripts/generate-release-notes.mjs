import { readFile, readdir, writeFile } from "node:fs/promises";
import { createHash } from "node:crypto";
import path from "node:path";
import { fileURLToPath } from "node:url";

const root = path.resolve(path.dirname(fileURLToPath(import.meta.url)), "..");
const requirementsDirectory = path.join(root, "Requirements");
const summariesPath = path.join(root, "scripts", "release-notes.json");
const outputPath = path.join(root, "wwwroot", "js", "shared", "release-notes-data.js");
const jsonOutputPath = path.join(root, "wwwroot", "release-notes-data.json");
const versionOutputPath = path.join(root, "wwwroot", "release-notes-version.json");
const wwwrootDirectory = path.join(root, "wwwroot");
const filePattern = /^(\d{4}-\d{2}-\d{2}) - Requirements - Day (\d+)\.txt$/;
const checkOnly = process.argv.includes("--check");

const summaries = JSON.parse(await readFile(summariesPath, "utf8"));
if (!Array.isArray(summaries)) throw new Error("scripts/release-notes.json must contain an array.");

const summaryByDay = new Map();
for (const summary of summaries) {
  const day = Number(summary?.day || 0);
  if (!day || summaryByDay.has(day)) throw new Error(`Release-note summary has an invalid or duplicate day: ${day || "unknown"}.`);
  validateSummary(summary);
  summaryByDay.set(day, summary);
}

const sourceFiles = (await readdir(requirementsDirectory))
  .map(name => ({ name, match: name.match(filePattern) }))
  .filter(item => item.match)
  .map(item => ({
    name: item.name,
    date: item.match[1],
    day: Number(item.match[2])
  }))
  .filter(item => item.day >= 1)
  .sort((left, right) => right.date.localeCompare(left.date) || right.day - left.day);

const sourceDays = new Set(sourceFiles.map(file => file.day));
const missingSummaries = sourceFiles.filter(file => !summaryByDay.has(file.day));
const orphanedSummaries = summaries.filter(summary => !sourceDays.has(Number(summary.day)));
if (missingSummaries.length) {
  throw new Error(`Missing release-note summaries for: ${missingSummaries.map(file => file.name).join(", ")}`);
}
if (orphanedSummaries.length) {
  throw new Error(`Release-note summaries have no matching prompt file for Day: ${orphanedSummaries.map(summary => summary.day).join(", ")}`);
}

const releaseNotes = [];
for (const source of sourceFiles) {
  const summary = summaryByDay.get(source.day);
  const prompt = normalizeLineEndings(await readFile(path.join(requirementsDirectory, source.name), "utf8")).trimEnd();
  const title = summary.title.trim();
  const sections = summary.sections.map(section => ({
    title: section.title.trim(),
    items: section.items.map(item => item.trim())
  }));
  const version = createHash("sha256")
    .update(JSON.stringify({ title, sections, prompt }))
    .digest("hex")
    .slice(0, 12);
  const id = `${source.date}-day-${source.day}`;
  releaseNotes.push({
    id,
    version,
    seenToken: `${id}@${version}`,
    date: source.date,
    day: source.day,
    title,
    sourceFile: source.name,
    sections,
    prompt
  });
}
if (!releaseNotes.length) throw new Error("No release-note prompt files were found.");

const serialized = JSON.stringify(releaseNotes, null, 2)
  .replaceAll("\u2028", "\\u2028")
  .replaceAll("\u2029", "\\u2029");
const output = `// Generated by scripts/generate-release-notes.mjs. Do not edit by hand.\nexport const releaseNotes = Object.freeze(${serialized});\n`;
const jsonOutput = `${serialized}\n`;
const versionOutput = `${JSON.stringify({ seenToken: releaseNotes[0].seenToken }, null, 2)}\n`;
const cacheUrl = `?v=release-notes-${releaseNotes[0].id}-${releaseNotes[0].version}`;
const cacheUrlPattern = /\?v=(?:release-notes-\d{4}-\d{2}-\d{2}-day-\d+(?:-[a-f0-9]{12})?|20260716-release-notes(?:-2)?)/g;
const cacheFiles = (await listFiles(wwwrootDirectory))
  .filter(file => file !== outputPath && [".css", ".html", ".js"].includes(path.extname(file)));
const cacheUpdates = [];
for (const file of cacheFiles) {
  const current = await readFile(file, "utf8");
  const updated = current.replace(cacheUrlPattern, cacheUrl);
  if (updated !== current) cacheUpdates.push({ file, updated });
}

if (checkOnly) {
  const current = await readFile(outputPath, "utf8").catch(() => "");
  if (current !== output) {
    throw new Error("Release-note data is out of date. Run npm run generate:release-notes.");
  }
  const currentJson = await readFile(jsonOutputPath, "utf8").catch(() => "");
  if (currentJson !== jsonOutput) {
    throw new Error("Release-note JSON feed is out of date. Run npm run generate:release-notes.");
  }
  const currentVersion = await readFile(versionOutputPath, "utf8").catch(() => "");
  if (currentVersion !== versionOutput) {
    throw new Error("Release-note version manifest is out of date. Run npm run generate:release-notes.");
  }
  if (cacheUpdates.length) {
    throw new Error(`Release-note browser cache keys are out of date in: ${cacheUpdates.map(item => path.relative(root, item.file)).join(", ")}`);
  }
  console.log(`Release-note data is current (${releaseNotes.length} releases).`);
} else {
  await Promise.all([
    writeFile(outputPath, output, "utf8"),
    writeFile(jsonOutputPath, jsonOutput, "utf8"),
    writeFile(versionOutputPath, versionOutput, "utf8")
  ]);
  await Promise.all(cacheUpdates.map(item => writeFile(item.file, item.updated, "utf8")));
  console.log(`Generated ${releaseNotes.length} releases and synchronized browser cache key ${cacheUrl.slice(3)}.`);
}

function validateSummary(summary) {
  if (typeof summary.title !== "string" || !summary.title.trim()) {
    throw new Error(`Day ${summary.day} needs a release title.`);
  }
  if (!Array.isArray(summary.sections) || !summary.sections.length) {
    throw new Error(`Day ${summary.day} needs at least one release-note section.`);
  }
  for (const section of summary.sections) {
    if (typeof section?.title !== "string" || !section.title.trim()) {
      throw new Error(`Day ${summary.day} has a section without a title.`);
    }
    if (!Array.isArray(section.items) || !section.items.length || section.items.some(item => typeof item !== "string" || !item.trim())) {
      throw new Error(`Day ${summary.day}, section "${section.title}", needs non-empty release-note items.`);
    }
  }
}

function normalizeLineEndings(value) {
  return String(value || "").replace(/\r\n?/g, "\n");
}

async function listFiles(directory) {
  const files = [];
  for (const entry of await readdir(directory, { withFileTypes: true })) {
    const entryPath = path.join(directory, entry.name);
    if (entry.isDirectory()) files.push(...await listFiles(entryPath));
    else files.push(entryPath);
  }
  return files;
}
